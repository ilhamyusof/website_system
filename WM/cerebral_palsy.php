        									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Premature Birth  - </strong> Children who were born before the 37th week of pregnancy, especially if they were born before the 32nd week of pregnancy, have a greater chance of having CP.</li>
        									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Multiple births  - </strong> Twins, triplets, and other multiple births have a higher risk for CP, especially if a baby’s twin or triplet dies before birth or shortly after birth.</li>
        									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Jaundice and kernicterus  - </strong> When too much bilirubin builds up in a new baby’s body, the skin and whites of the eyes might look yellow. This yellow coloring is called jaundice. When severe jaundice goes untreated for too long, it can cause a condition called kernicterus. This can cause CP and other conditions.</li>
        								</ul>
