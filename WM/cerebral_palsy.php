    									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Infections during pregnancy  -</strong> Infections can lead to increases in certain proteins called cytokines that circulate in the brain and blood of the baby during pregnancy. Cytokines cause inflammation, which can lead to brain damage in the baby. Fever in the mother during pregnancy or delivery also can cause this problem.</li>
    									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Medical conditions of the mother -</strong> Mothers with thyroid problems, intellectual disability, or seizures have a slightly higher risk of having a child with CP.</li>
    									<li style="text-align: justify; color :#FFF"><i class='bx bx-check-circle' style="color :#FFF"></i><strong style="text-transform: uppercase;">Birth complications -</strong> Detachment of the placenta, uterine rupture, or problems with the umbilical cord during birth can disrupt oxygen supply to the baby and result in CP.</li>
